package arithmeticPro;

public class SwitchCase {
	
	public static void main(String[] args) {
		
		
		String a="chrome" ;
		
		switch(a) {
		
		case "chrome" : System.out.println("hii"); break;
			
		case "firefox": System.out.println("hello"); break;
			
			
		case "edge": System.out.println("hii-hello;"); break;
		}
		
		
		
        int b= 2 ;
		
		switch(b) {
		
		case 1 : System.out.println("hii"); break;
			
		case 2: System.out.println("hello"); break;
			
			
		case 3: System.out.println("hii-hello;"); break;
		}
		
	}

}
